def printAP(num1,num2):
    print(f"{num1} + {num2} = {num1+num2}")
    print(f"{num1} - {num2} = {num1-num2}")
    print(f"{num1} * {num2} = {num1*num2}")
    print(f"{num1} / {num2} = {num1/num2}")

printAP(3,4)


