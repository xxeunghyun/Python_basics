package numberstring;

public class CharString {

	public static void main(String[] args) {
		

		System.out.println('생');	// char '' O
		System.out.println("생활코딩");	//string "" O
		//System.out.println('생활코딩');	//string '' X
		System.out.println("생활코딩" + "입니다");	// string + string O
		
		
		System.out.println("\"생활코딩\"에 오신 것을 환영합니다");

	}

}
